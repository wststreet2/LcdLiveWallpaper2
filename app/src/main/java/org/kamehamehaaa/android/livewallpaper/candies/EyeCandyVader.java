	public boolean[][] draw(boolean[][] matrix) {
		
		if( pos-- <= 0-vader.length){
			pos = LCDLiveWallpaper.getLCD_HEIGHT();
		}
		
		drawSmall(matrix);
		
		return matrix;
	}

	private void drawSmall(boolean[][] matrix) {
		int startw = (LCDLiveWallpaper.getLCD_WIDTH() / 2) - vader[0].length/2;
		int starth = pos; // 46; // (LCDLiveWallpaper.getLCD_HEIGHT() / 2) - 14;
		
		for (int i = 0; i < vader[0].length; i++)
			for (int j = 0; j < vader.length; j++) {
				try {
					if (vader[j][i] == 1) {
						matrix[i + startw][j + starth] = true;
					} else if (vader[j][i] == 2) {
						matrix[i + startw][j + starth] = false;
					}
				} catch (Exception e) {
				}
			}

	}

